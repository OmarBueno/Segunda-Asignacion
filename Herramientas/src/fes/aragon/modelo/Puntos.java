package fes.aragon.modelo;

public class Puntos {
	private int xx;
	private int yy;

	public Puntos(int xx, int yy) {
		super();
		this.xx = xx;
		this.yy = yy;
	}

	public int getXx() {
		return xx;
	}

	public void setXx(int xx) {
		this.xx = xx;
	}

	public int getYy() {
		return yy;
	}

	public void setYy(int yy) {
		this.yy = yy;
	}

}
